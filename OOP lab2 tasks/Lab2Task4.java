class Lab2Task4
{
 public static void main(String [] args)
 {
  float rad= 10.1f;
  float dia= 2*rad;
  float pi= 3.14f; 
  double area=pi*rad*rad;
  double circ=pi*dia;
  System.out.println("Radius       :"+rad);
  System.out.println("Diameter     :"+dia);
  System.out.println("Area         :"+area);
  System.out.println("Circumference:"+circ);

 }
}
  